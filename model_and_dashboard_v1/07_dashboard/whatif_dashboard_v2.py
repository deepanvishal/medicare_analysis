def base_rate_share(spec):
    base_v = _total_members * BASE_RATE.get(spec, 0.0)
    cond_v = sum(float(_cond_footprint.get(c, 0.0)) * r[spec]
                 for c, r in RATE_LOOKUP.items() if spec in r)
    cond_v += _other_footprint * OTHER_RATE.get(spec, 0.0)
    total = base_v + cond_v
    return base_v / total if total else None


