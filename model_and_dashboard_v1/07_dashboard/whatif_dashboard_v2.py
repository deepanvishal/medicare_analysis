def _extract_dir():
    try:
        here = os.path.dirname(os.path.abspath(__file__))
    except NameError:
        here = os.getcwd()
    cand = os.path.join(here, "extracts")
    if os.path.isdir(cand):
        return cand
    probe = here
    for _ in range(6):
        cand = os.path.join(probe, "model_and_dashboard_v1", "07_dashboard",
                            "extracts")
        if os.path.isdir(cand):
            return cand
        probe = os.path.dirname(probe)
    raise FileNotFoundError(
        "extracts folder not found - run 21_dashboard_extracts.py first")


def load_extracts():
    d = _extract_dir()
    names = ["enrollment", "growth_context", "sickness_rates", "visit_rates",
             "county_calibration", "providers", "conditions_meta",
             "other_condition_prevalence"]
    frames = {}
    for n in names:
        path = os.path.join(d, f"{n}.parquet")
        if not os.path.isfile(path):
            raise FileNotFoundError(
                f"{n}.parquet missing - regenerate the eight-file extract "
                f"set with 21_dashboard_extracts.py")
        frames[n] = pd.read_parquet(path)
    with open(os.path.join(d, "manifest.json"), "r", encoding="utf-8") as f:
        manifest = json.load(f)
    return frames, manifest


